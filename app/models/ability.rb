class Ability
  include CanCan::Ability

  def initialize(user)
    unless user.nil?
      can :read, :all
      can :manage, :all
      cannot :manage, Menu
      admin_ui
    end
  end

  def admin_ui
    can :access, :rails_admin   # grant access to rails_admin
    can :dashboard              # grant access to the dashboard
  end
end